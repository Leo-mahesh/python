#****************************************Data types *******************************************#
# 01) Numbers


#----------------Integer-----------------@
print()
print("-------------------------------------------Integer------------------------------------------")
a = 10  #(Whole number)
print(a) #(print statement)
print(type(a)) #(type)
print()

""" ------------Output------------

10
<class 'int'>

-----------------------------------"""

#---------------Float--------------------@
print()

print("-------------------------------------------Float------------------------------------------")
a = 10.5  #(float number)
print(a) #(print statement)
print(type(a)) #(type)
print()

""" ------------Output------------

10.5
<class 'float'>

-----------------------------------"""

#---------------Complex--------------------@
print()
print("-------------------------------------------Complex------------------------------------------")
a = 10j  #(complex number)
print(a) #(print statement)
print(type(a)) #(type)
print()

""" ------------Output------------

10j
<class 'complex'>

-----------------------------------"""

#---------------Boolean--------------------@
print()
